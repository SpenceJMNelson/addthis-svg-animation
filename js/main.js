$(window).scroll(function() {    
    var scroll = $(window).scrollTop();

    if (scroll >= 900) {
        $(".tool-illustration-wrapper").addClass("active");
    } else {
        $(".tool-illustration-wrapper").removeClass("active");
    }
});

